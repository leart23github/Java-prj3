    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

       try
       { 
        Connection cn= db.connector.getConnection();
        PreparedStatement ps=cn.prepareStatement("select * from infos1");
        ResultSet rs=ps.executeQuery();
        DefaultTableModel tm=(DefaultTableModel)jTable1.getModel();
        tm.setRowCount(0);
       while(rs.next())
       {
          Object o[]={rs.getString("type"),rs.getString("name"),rs.getString("artist"),rs.getString("title"),rs.getString("size"),rs.getString("duration"),rs.getString("author"),rs.getString("numberOfPages"),rs.getString("director")};
             tm.addRow(o);
       }
       }
       catch(Exception e)
       {
       JOptionPane.showMessageDialog(this,e);
       }
        
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String name = jTextField1.getText();
        String artist = jTextField2.getText();
        String title = jTextField3.getText();
        String size = jTextField6.getText();
        String duration = jTextField5.getText();
        String author = jTextField7.getText();
        String numberOfPages = jTextField8.getText();
        String director = jTextField9.getText();
        
        String type = null;
        if(jRadioButton1.isSelected()){
            type = "Music CD";
        }
        if(jRadioButton2.isSelected()){
            type = "Video DVD";
        }
        if(jRadioButton3.isSelected()){
            type = "EBook";
        }
        
        /*MusicCD music1 = new MusicCD("CD",name,artist,title,size,duration);
        VideoDVD video1 = new VideoDVD("DVD", name,director,size,duration);
        EBook ebook1 = new EBook("EBook", name,author,size,numberOfPages);*/
      
        try{
            Connection cn=db.connector.getConnection();
            String sql ="INSERT INTO 'infos1' (type,name,artist,title,size,duration,author,numberOfPages,director) VALUES (?,?,?,?,?,?,?,?,?)";
            PreparedStatement ps=cn.prepareStatement(sql);
            ps.setString(1,type);
            ps.setString(2,name);
            ps.setString(3,artist);
            ps.setString(4,title);
            ps.setString(5,size);
            ps.setString(6,duration);
            ps.setString(7,author);
            ps.setString(8,numberOfPages);
            ps.setString(9,director);
            ps.executeUpdate();
            JOptionPane.showMessageDialog(this,"saved successfully");
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(this,e);
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

